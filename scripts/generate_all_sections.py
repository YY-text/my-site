def get_all_sections(course_info):
    sections = []
    for chapter in course_info['chapters']:
        for section in chapter['sections']:
            section['chapter'] = chapter
            sections.append(section)
    return sections


def main():
    base_path = "/workspace/courses"
    total_count = 0

    for course_key, course_info in COURSES.items():
        course_dir = os.path.join(base_path, course_key)
        os.makedirs(course_dir, exist_ok=True)

        all_sections = get_all_sections(course_info)

        for i, section in enumerate(all_sections):
            prev_section = all_sections[i-1] if i > 0 else None
            next_section = all_sections[i+1] if i < len(all_sections)-1 else None

            chapter = section['chapter']

            # 生成小节详情页
            html = generate_section_html(course_key, course_info, chapter, section, prev_section, next_section)
            filename = f"section-{section['id']}.html"
            filepath = os.path.join(course_dir, filename)

            with open(filepath, 'w', encoding='utf-8') as f:
                f.write(html)

            total_count += 1
            print(f"生成: {course_key}/{filename}")

    print(f"\n完成！共生成 {total_count} 个小节页面！")


